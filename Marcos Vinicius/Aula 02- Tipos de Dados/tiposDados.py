#Operador Soma
numero1 = 5
numero2 = 5
soma = numero1 + numero2
print('A soma é igual a ' , soma)
print('A soma é igual a ' + str(soma))
print(f'A soma é igual a {soma}')

#Operador Subtração
numeroSub1 = 10
numeroSub2 = 5
subtracao = numeroSub1 - numeroSub2
print(f'A subtração é igual a {subtracao}')
print('A subtração é igual a ' + str(subtracao))

#Operador Multiplicação
numeroMult1 = int(input("Digite um valor entre 0 a 10: "))
numeroMult2 = int(input("Digite um valor entre 0 a 10: "))
multiplicacao = numeroMult1 * numeroMult2
print(f'A multiplicação é igual a {multiplicacao}')

#Operador Divisão
numeroDiv1 = int(input("Digite um valor entre 0 a 10: "))
numeroDiv2 = int(input("Digite um valor entre 0 a 10: "))
